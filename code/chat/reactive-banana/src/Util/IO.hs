module Util.IO (
    EventSource(..)
  , mkEventSource
  ) where

import           Control.Event.Handler

data EventSource a =
  EventSource {
    addHandler :: AddHandler a
  , fire       :: a -> IO ()
  }

mkEventSource :: IO (EventSource a)
mkEventSource =
  uncurry EventSource <$> newAddHandler
